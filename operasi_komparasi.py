#operasi komparasi

#>,<,>=,<=,==,!=,is,is not

a = 4
b = 2

#hasilnya bakal true karena nilai a lebih dari nilai 2.
hasil = a > 2
print("hasil = ", hasil)

#hasilnya bakalan false karena nilai a kurang dari nilai 1.
hasil = a < 1
print("hasil = ", hasil)

#haislnya bakalan false karena 2 tidak lebih dari 4
hasil = b > 4
print("hasil = ", hasil)

#hasilnya bakalan true karena 2 lebih dari 1
hasil = b > 1
print("hasil = ", hasil)

#== sama dengan perbandingan kedua nilai.
hasil = a == 2
print("hasil = ", hasil)

#!= kembalikan sama dengan.
hasil = b != 1
print("hasil = ", hasil)